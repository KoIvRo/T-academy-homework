        #общая информация
        print(f"{self.get_hangman_stage(game.get_picture_stage())}\nКатегория: {game.get_category()}\nСлово: {game.get_cur_state_word()}. Попыток осталось {game.get_attemps()}\nИспользованные буквы: {game.get_prev_letters()}")

